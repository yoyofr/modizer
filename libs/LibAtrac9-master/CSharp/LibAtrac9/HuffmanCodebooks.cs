        {
            var tables = new HuffmanCodebook[bits.Length][];
            for (int i = 0; i < tables.Length; i++)
            {
                if (codes[i] != null)
                {
                    tables[i] = GenerateHuffmanCodebooks(codes[i], bits[i], groupCounts[i]);
                }
            }
            return tables;
        }

